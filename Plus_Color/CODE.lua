local function Init_Menu(self, root)
    if not self:IsVisible() then
        return
    end

    local sub
    for _, name in pairs(Tab) do
        local color = _G[name..'_CODE']
        if color and _G[name] then
            sub=root:CreateRadio (
                color..name,
            function(data)
                return self:GetParent():GetText()==data.name
            end, function(data)
                local edit= self:GetParent()
                edit:SetText(data.name..'_CODE')
                edit:Setup()
                return MenuResponse.Refresh
            end, {name=name})
            sub:SetTooltip(function(tooltip, desc)
                tooltip:AddLine(name..'_CODE')
                tooltip:AddDoubleLine('|c',_G[name..'_CODE']:gsub('|c', ''), 1,1,1, 1,1,1)
            end)
        end
    end

--SetScrollMod
    WoWTools_MenuMixin:SetScrollMode(root)
end


local function Init(frame)
    local btn= WoWTools_ButtonMixin:Menu(frame, {atlas='AnimaChannel-Bar-Necrolord-Gem'})
    
    btn:SetPoint('RIGHT', frame.Instructions, 'LEFT')
    btn:SetupMenu(Init_Menu)
end



function WoWTools_ColorMixin:Init_CODE(frame)
    Init(frame)
end